package Lesson8;

import com.fasterxml.jackson.annotation.JsonProperty;

public class People {
    @JsonProperty
    private String name;

    @JsonProperty
    private int age;

    public People(){}

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    @Override
    public String toString() {
        return "People{" +
                "name='" + name + '\'' +
                ", age=" + age +
                '}';
    }
}
